                                <h2>What is the CS Fair?</h2>
                                <p>
                                    The CS Fair is a UVM event where Computer Science students (majors, minors or just taking a class) may submit and showcase their computer projects. The purpose of the CS Fair is to give students a chance to present their work to their peers and professionals in an open and exciting environment. A panel of judges critique the student work where the prize amounts are up to $300 with many opportunities to win. This year the CS Fair will take place in the Davis Center in the Grand Maple Ballroom.
                                </p>
                                <p>The idea for the CS Fair was born in Robert Erickson's head while hanging out in the students' club room sometime in 2012. After asking several questions about why their projects were not working, Robert commented on what cool projects they were. Robert's mind chewed on the fact that it would be cool if other people besides this small group had a chance to see these awesome projects.</p>

                                <p>Robert said if he worked on creating a fair of some kind so they could demonstrate their projects to a larger audience, would they be interested? Everyone thought that would be great.</p>

                                <p>Shortly after meeting with the students, at a CS faculty meeting, then department chair Professor Maggie Epstein asked if someone would be interested in organizing a student project demonstration of some kind. Before she finished her sentence, Robert's hand was up. Maggie gave Robert the go ahead and approved all of his crazy ideas, thus the CS Fair was born!</p>
